/**
 * add - adds two numbers
 *
 * @x: first number
 * @y: second number
 *
 * Return: integer
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - subtracts two numbers
 *
 * @x: first number
 * @y: second number
 *
 * Return: integer
 */
int sub(int x, int y)
{
	return (x - y);
}

/**
 * mul - multiplies two numbers
 *
 * @x: first number
 * @y: second number
 *
 * Return: integer
 */
int mul(int x, int y)
{
	return (x * y);
}

/**
 * div - divides two numbers
 *
 * @x: first number
 * @y: second number
 *
 * Return: integer
 */
int div(int x, int y)
{
	return (x / y);
}

/**
 * mod - returns the remainder of two numbers
 *
 * @x: first number
 * @y: second number
 *
 * Return: integer
 */
int mod(int x, int y)
{
	return (x % y);
}
